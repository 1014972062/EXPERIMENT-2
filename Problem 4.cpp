#include <iostream>
#include <conio.h>
#include <iomanip>
using namespace std;
int main()
{
	int h;
	h=1;
	 cout<<"Counting starting...\n";
	while (h<10)
	{
		cout << h;
		cout << "," ;
		h++;
	}	
		
	while (h<=28)
	{
		cout<<h;
		cout<< ",";
		h=h+2;
	}
	while (h=30)
{
	cout<<h;
	break;
}
	_getch();
	return 0;
		
	
}
